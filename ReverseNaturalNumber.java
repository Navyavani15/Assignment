//5) program to print first 10 natural numbers in reverse order

import java.util.*;
class ReverseNaturalNumber
{
	public static void main(String args[])
	{
		int n=10;
		System.out.println("First 10  natural numers in reverse order");
		while(n>0)
		{
			System.out.println(+n);
			n--;
		}
	}
}